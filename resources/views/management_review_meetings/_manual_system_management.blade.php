<h4>5.0 Politica integrată de calitate, de mediu şi OHSAS</h4>
<p>
    Managementul firmei noastre este angajat pentru implementarea şi inbunătăţirea continuă a unui sistem de management integrat al calităţii, de mediu şi OHSAS, care se bazează pe determinarea corectă şi satisfacerea deplină ale cerinţelor clienţilor, având în vedere şi identificarea şi evaluarea aspectelor de mediu şi a riscurilor privind sănătatea şi securitatea în muncă legate de activitatea noastră.
</p>
<p>
    Consolidarea poziţiei noastre pe piaţă, precum şi îmbunătăţirea performanţelor de mediu şi a celor referitoare la sănătatea ocupaţională asigurăm prin:
</p>
<ul>
    <li>
        producerea de confecţii metalice de un înalt nivel de calitate precum şi realizarea unor structuri metalice şi construcţii civile şi industriale care să corespundă cerinţelor
    </li>
    <li>
        asigurea creşterii continue a satisfacţiei clienţilor prin oferirea de produse şi servicii cu grad înalt de calitate
    </li>
    <li>
        Conformarea cu legislaţia şi cu reglementările din domeniul calităţii, al  mediului şi OHSAS, aplicabile activităţilor noastre
    </li>
    <li>
        minimalizarea impacturilor asupra mediului
    </li>
    <li>
        minimalizarea riscurilor privind sănătatea şi securitatea ocupaţională pentru proprii angajati, contractori si vizitatori
    </li>
    <li>
        prevenirea poluării mediului prin promovarea şi implementarea celor mai bune tehnici disponibile privind activităţile noastre
    </li>
    <li>
        protejarea mediul înconjurător în principal prin reducerea atât a emisiilor cât si a utilizării resurselor naturale neregenerabile
    </li>
    <li>
        îmbunătăţirea performanţelor de mediu şi OHSAS prin aplicarea unor măsuri planificate şi sistematice, care să asigure ţinerea sub control al aspectelor de mediu şi al riscurilor OHSAS identificate
    </li>

    <p>
        Conştientizăm necesitatea unui comportament responsabil din partea fiecărui angajat privind protejarea mediului, şi reducerea riscurilor la locurile de muncă şi prevenirea accidentelor.
    </p>
    <p>
        Politica noastră este comunicată tuturor angajaţilor şi tutror subcontrctanţilor noştri şi este accesibilă părţilor interesate. 
    </p>
</ul>
