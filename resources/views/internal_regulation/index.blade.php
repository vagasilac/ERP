            <p class="marginT30">
                <strong>Cap.VII.TIMPUL DE MUNCA</strong>
            </p>
            <p>
                7.1.Timpul de muncă reprezintă timpul pe care salariatul îl foloseşte
                pentru îndeplinirea sarcinilor de serviciu.
            </p>
            <p>
                7.2.Pentru salariaţii angajaţi cu normă întreagă, durata normală a timpului
                de muncă este de 8 ore pe zi şi de 40 de ore pe săptămână. Programul de
                lucru în unitate este intre orele 7 -15,30. Ȋn durata normală a timpului de
                muncă nu intră timpii consumaţi cu echiparea-dezechiparea la începutul şi
                sfârşitul programului.
            </p>
            <p>
                7.3.Ȋn cazul tinerilor în vârstă de până la 18 ani, durata timpului de
                muncă este de 6 ore pe zi şi de 30 ore pe săptămână.
            </p>
            <p>
                7.4.Pentru anumite activităţi, se poate stabili prin negociere colectivă
                sau individuală, o durată zilnică mai mare sau mai mică de 8 ore.
            </p>
            <p>
                Durata zilnică a timpului de muncă de 12 ore va fi urmată de o perioadă de
                repaus de 24 ore.
            </p>
            <p>
                7.5.Programul de lucru se poate modifica la iniţiativa unităţii, fără a se
                modifica numărul de ore lucrate şi pauzele, noul program va fi afişat la
                locul de muncă.
            </p>
            <p>
                a)Evidenţa prezenţei la locul de muncă se va efectua prin metoda pontajului
                , documentul utilizat pentru evidenţa la lucru este foaia colectivă de
                prezenţă.
            </p>
            <p>
                b)Conducătorilor formaţiilor de lucru care nu completează corect foaia de
                prezenţă colectivă li se aplică sancţiunile disciplinare prevăzute în
                prezentul regulament.
            </p>
            <p>
                7.6.
            </p>
            <p>
                a) Durata maximă legală a timpului de muncă nu poate depăşi 48 de ore pe
                săptămână, inclusiv orele suplimentare.
            </p>
            <p>
                b) Munca prestată în afara duratei normale a timpului de muncă săptămânal
                este considerată muncă suplimentară.
            </p>
            <p>
                c) Munca suplimentară se compensează prin ore libere plătite, în
                următoarele 60 de zile calendaristice. Ȋn aceste condiţii salariatul
                beneficiază de salariul corespunzător pentru orele prestate peste programul
                normal de lucru.
            </p>
            <p>
                d) Ȋn cazul în care compensarea prin ore libere nu este posibilă în
                termenul prevăzut la litera c) munca suplimentară va fi plătită cu un spor
                de 75% la salariul corespunzător duratei acesteia.
            </p>
            <p>
                7.7.Salariaţii care efectuează muncă de noapte beneficiază de un program de
                lucru redus cu o oră faţă de durata normală a zilei de muncă, fără ca
                aceasta să ducă la scăderea salariului de bază. Munca prestată între orele
                23,00- 7,00 este considerată muncă de noapte.
            </p>
            <p>
                7.8. Organizarea societăţii este structurată în baza organigramei, aprobată
                de conducere. Prin aceasta se efectuează repartizarea salariaţilor pe
                locuri de muncă cu precizarea atribuţiilor şi răspunderilor prin fişa de
                post, cu elaborarea de norme de muncă, de timp şi norme de producţie,
                însoţite de descrierea sferei de atribuţii, fundamentate din punct de
                vedere tehnic, menite să asigure utlizarea integrală şi eficientă a
                timpului de lucru.
            </p>
            <p>
                Normarea muncii se aplică tuturor categoriilor de angajaţi.
            </p>
            <p>
                7.9.Durata pauzei de masă este de 30 minute, între orele 11 şi 11,30, şi nu
                este inclusă în programul de lucru.
            </p>
            <p>
                7.10.
            </p>
            <p>
                a)Repausul săptămânal se acordă în doua zile consecutive, de regulă sâmbăta
                şi duminica.
            </p>
            <p>
                b) Când desfăşurarea normală a activităţii impune efectuarea muncii,
