            <p class="marginT30">
                <strong>
                    Cap.IX. PROCEDURA DE SOLUŢIONARE A CERERILOR SAU RECLAMAŢIILOR
                </strong>
            </p>
            <p align="justify">
                9.1 Salariaţii au dreptul de a adresa conducerii societăţii, în scris,
                petiţii individuale, dar numai în legătură cu problemele proprii apărute la
                locul de muncă şi în activitatea desfăşurată. Prin petiţii se înţelege
                cererile sau reclamaţiile individuale pe care un salariat le adresează
                conducerii societăţii, în condiţiile legii şi ale regulamentului intern.
                Petiţiilor anonime nu li se va da curs, acestea fiind clasate.
            </p>
            <p align="justify">
                9.2 Cererile sau reclamaţiile individuale vor fi adresate administratorului
                societăţii şi vor fi înregistrate la secretariat. Administratorul
                societăţii are obligaţia să comunice salariatului răspunsul său în termen
                de 30 de zile de la data primirii cererii sau a reclamaţiei, indiferent
                dacă soluţia este favorabilă sau nefavorabilă. În situaţia în care
                aspectele sesizate prin petiţia salariatului necesită o cercetare mai
                amănunţită, angajatorul poate prelungi termenul cu cel mult 15 zile.
            </p>
            <p align="justify">
                9.3 Salariaţii nu pot formula două petiţii privitoare la aceeaşi problemă.
                În situaţia în care un salariat adresează în aceeaşi perioadă de timp mai
                multe petiţii, cu acelaşi obiect, acestea se vor conexa, salariatul urmând
                să primească un singur răspuns. Dacă, după trimiterea răspunsului, se
                primeşte o nouă petiţie cu acelaşi conţinut sau care priveşte aceeaşi
                problemă, aceasta se clasează la numărul iniţial, făcându-se menţiune că
                s-a răspuns.
            </p>
            <p class="marginT30">
                <strong>
                    Cap.X. REGULI CONCRETE PRIVIND DISCIPLINA MUNCII ȊN SOCIETATE
                </strong>
            </p>
            <p>
                10.1.Să respecte programul de lucru.
            </p>
            <p>
                10.2.Să vină la locul de muncă în condiţii fizice şi psihice
                corespunzătoare pentru a putea executa în bune condiţii sarcinile ce le
                revin, prezentându-se şi părăsind serviciul la orele stabilite.
            </p>
            <p>
                10.3.Să primească şi să îndeplinească corect şi la timp îndatoririle ce ii
                revin la locul de muncă.
            </p>
            <p>
                10.4.Să cunoască caracteristicile constructive şi funcţionale ale
                utilajelor, instalaţiilor şi maşinilor la
            </p>
            <p>
                care lucrează, să aplice întocmai normele de funcţionare şi întreţinere ale
                acestora.
            </p>
            <p>
                10.5.Să înştiinţeze şeful ierarhic superior de îndată ce au luat la
                cunoştinţă de existenţa unor nereguli, abateri, greutăţi sau lipsuri în
                aprovizionarea locului de muncă, în întreţinerea sau repararea maşinilor
                sau utilajelor etc. şi să propună măsuri, în raport cu posibilităţile pe
                care le au, pentru prevenirea în viitor a unor asemenea situaţii.
            </p>
            <p>
                10.6.Să respecte norma de muncă ,de consum şi de calitate stabilite.
            </p>
            <p>
                10.7.Să execute toate îndatoririle ce îi revin la locul de muncă, fiind
                răspunzator de îndeplinirea lor corectă şi la timp.
            </p>
            <p>
                10.8.Să cunoască şi să respecte normele de protecţie a muncii şi de PSI, să
                utilizeze şi să păstreze în mod corespunzător echipamentul de protecţie şi
                de muncă.
            </p>
            <p>
                10.9.Să folosească timpul de lucru pentru îndeplinirea sarcinilor de
                serviciu.
            </p>
            <p>
                10.10 Să se ponteze cu cardul de pontaj la intrarea la locul de muncă şi la
                terminarea programului de lucru şi ieşirea de la locul de muncă. Echiparea
                şi dezechiparea de echipamentul de lucru se face înainte de intrare şi după
                ieşirea de la locul de muncă.
            </p>
            <p>
                10.11.Să părăsească locul de munca numai cu aprobarea şefului direct.
            </p>
            <p>
                10.12.Să nu lase maşinile în funcţiune fără supraveghere şi să anunţe orice
                defecţiune.
            </p>
            <p>
                10.13.Să respecte timpul acordat pauzelor de masă.
            </p>
            <p>
                10.14.Să păstreze curăţenia şi ordinea la locul de muncă, să poarte hainele
                de lucru regulamentar.
            </p>
            <p>
                10.15.Să folosească vestiarele existente.
            </p>
            <p>
                10.16.Lucrul încetează la terminarea programului.
            </p>
            <p>
                10.17.Delimitarea atribuţiilor, răspunderilor şi competenţelor legate de
                obligaţiile ce decurg din acest regulament se face în conformitate cu fişa
                postului.
            </p>
            <p>
                10.18.La solicitarea şefului ierarhic, să se supună controlului cu detector
                de alcool, iar în caz de refuz să se prezinte la analiza sângelui în
                vederea determinării concentraţiei de alcool în sânge.
            </p>
            <p>
                10.19.Persoanele angajate nu se pot apăra de răspundere pe motiv că nu
                cunosc actele normative şi regulamentele adoptate, dacă acestea le-au fost
                aduse la cunoştinţă prin publicare, afişare, comunicare şi s-au luat la
                cunoştinţă prin semnarea individuală.
            </p>
            <p>
                10.20.Ȋn timpul executării atribuţiilor şi sarcinilor de serviciu, fiecare
                angajat trebuie să respecte ierarhia funcţiilor din cadrul societăţii, dar
                totodată şi principiul unicităţii conducerii, fiecare angajat având un şef
                direct.
            </p>
            <p class="marginT30">
                <strong>Cap.XI. ABATERILE DISCIPLINARE ṢI SANCŢIUNILE APLICATE</strong>
            </p>
            <p>
                11.1.<strong>Abaterea disciplinară</strong> este o faptă în legătură cu
                munca şi care constă într-o acţiune sau inacţiune săvârşită cu vinovăţie de
                către salariat, prin care acesta a încălcat normele legale, regulamentul
                intern, contractul individual de muncă sau contractul colectiv de muncă
                aplicabil, ordinele şi dispoziţiile legale ale conducătorilor ierarhici.
                Răspunderea penală, administrativă sau materială nu exclude răspunderea
                disciplinară pentru fapta săvârşită dacă prin aceasta s-au încălcat
                obligaţiile de muncă.
            </p>
            <p>
                11.2.Sancţiunile disciplinare pe care le poate aplica angajatorul în cazul
                în care salariatul săvârşeşte o abatere disciplinară sunt:
            </p>
            <p>
                a) avertisment scris;
            </p>
            <p>
                b) retragerea din funcţie,cu acordarea salariului corespunzător funcţiei în
                care s-a dispus
            </p>
            <p>
                retrogradarea, pentru o durată ce nu poate depăşi 60 zile;
            </p>
            <p>
                c) diminuarea salariului de bază pe o durată de 1-3 luni cu 5-10%;
            </p>
            <p>
                d) reducerea salariului de bază pe o durată şi a îndemnizaţiei de conducere
                pe o perioadă de 1-3 luni cu 5-10%;
            </p>
            <p>
                e)desfacerea disciplinară a contractului individual de muncă.
            </p>
            <p>
                11.3.Amenzile disciplinare sunt interzise. Pentru aceeaşi abatere
                disciplinară se poate aplica numai o singură sancţiune.
            </p>
            <p>
                11.4.Angajatorul stabileşte sancţiunea disciplinară aplicabilă în raport cu
                gravitatea abaterii
            </p>
            <p>
                disciplinare săvârşite de salariat, avându-se în vedere următoarele:
            </p>
            <p>
                a) imprejurările în care fapta a fost săvârşită;
            </p>
            <p>
                b) gradul de vinovăţie a salariatului;
            </p>
            <p>
                c) consecinţele abaterii disciplinare;
            </p>
            <p>
                d) comportarea generală în serviciu a salariatului;
            </p>
            <p>
                e) eventuale sancţiuni disciplinare suferite anterior de către acesta.
            </p>
            <p align="justify">
                11.4.1<strong> Avertismentul scris </strong>–este încunoştinţarea scrisă
                făcută angajatului, pentru abateri disciplinare de muncă mai puţin grave,
                săvârşite pentru prima dată de către acesta şi care nu aduc prejudicii
                materiale unităţii, ca de exemplu:
            </p>
            <p>
                a) nefolosirea echipamentului de protecţie corespunzător;
            </p>
            <p>
                b) nefolosirea echipamentului individual de lucru;
            </p>
            <p>
                c) nepăstrarea ordinei şi curăţeniei la locul de muncă;
            </p>
            <p>
                d) greşeli de mică importanţă în executarea unei lucrări care nu produce
                prejudicii societăţii;
            </p>
            <p>
                e) întârzierea la serviciu sub o oră, cu efectul aferent asupra salariului
                cuvenit pentru timpul lucrat;
            </p>
            <p>
                f) neexecutarea în termen sau executarea necorespunzătoare a lucrărilor
                dispuse de către şeful direct, pentru prima dată;
            </p>
            <p>
                g) refuzarea folosirii sondei de alcool, la solicitarea şefilor ierarhici
                superiori;
            </p>
            <p>
                h) plecarea de la serviciu fără aprobare pentru prima dată, nesemnalarea
                unor nereguli, abateri, defecţiuni atunci când acestea intră în obligaţia
                de serviciu a salariatului;
            </p>
            <p>
                i) alte fapte de minimă importanţă comise din culpă de către angajaţi la
                locul de muncă sau în legătură cu munca lor.
            </p>
            <p>
                11.4.2<strong> Reducerea salariului de bază</strong>-se aplică pentru
                abateri disciplinare mai grave de natură să
            </p>
            <p>
                determine bunul mers al activităţii societăţii, rezultate din
                neîndeplinirea cu vinovăţie a obligaţiilor individuale de serviciu ce revin
                fiecăruia,ca de exemplu:
            </p>
            <p>
                a) repetarea abaterilor pentru care a primit un avertisment;
            </p>
            <p>
                b) absenţe nemotivate de o zi;
            </p>
            <p>
                c) părăsirea locului de muncă pentru a doua oară fără aprobarea şefului
                direct;
            </p>
            <p>
                d) neexecutarea sarcinilor de serviciu primite;
            </p>
            <p>
                e) încălcarea cu vinovăţie pentru prima dată a obligaţiilor cuprinse în
                prezentul regulament (secretul de serviciu);
            </p>
            <p>
                f) insulte, ameninţări şi calomnii aduse colegilor sau superiorilor
                ierarhici în legătură cu serviciul;
            </p>
            <p>
                g) alte fapte comise da angajaţi la locul de muncă sau în legatură cu munca
                dacă în raport cu
            </p>
            <p>
                circumstanţele cauzei se impune aplicarea unei atare sancţiuni.
            </p>
            <p>
                11.4.3
                <strong>
                    Desfacerea disciplinară a contractului individual de muncă
                </strong>
                cu pierderea dreptului de preaviz este sancţiunea cea mai gravă ce se
                aplică salariaţilor pentru abateri grave care aduc sau pot aduce pagube de
                orice natură societăţii, ca de exemplu:
            </p>
            <p>
                a) prezentarea la serviciu sub influenţa băuturilor alcoolice sau consumul
                de alcool în timpul programului de lucru;
            </p>
            <p>
                a) salariatul a uzat de documente sau declaraţii nereale la angajare;
            </p>
            <p>
                b) a sustras obiecte, valori, documente, bunuri si altele, aparţinând
                societăţii;
            </p>
            <p>
                c) a fost absent nemotivat timp de 3 zile consecutive de la locul de muncă;
            </p>
            <p>
                d) a pus în pericol prin acte deliberate sau prin imprudenţă securitatea
                societăţii, a colaboratorilor sau provocare de avarii
            </p>
            <p>
                e) a falsificat un act generator de drepturi
            </p>
            <p>
                f) salariatul a comis abateri repetate de la regulile de disciplina.
            </p>
            <p>
                11.5.
            </p>
            <p>
                1).Sub sancţiunea nulităţii absolute, nici o măsură nu poate fi dispusă mai
                inainte de efectuarea unei cercetări disciplinare prealabile.
            </p>
            <p>
                2).Ȋn vederea desfăşurării cercetării disciplinare prealabile, salariatul
                va fi convocat în scris de persoana împuternicită de către angajator să
                realizeze cercetarea, precizându-se obiectul, data, ora şi locul
                întrevederii.
            </p>
            <p>
                3).Neprezentarea salariatului la convocarea făcuta în condiţiile prevăzute
                la alin.2. fără un motiv obiectiv, dă dreptul angajatorului să dispună
                sancţiunea, fără efectuarea cercetării disciplinare prealabile.
            </p>
            <p>
                4).Ȋn cursul cercetării disciplinare salariatul are dreptul să formuleze şi
                să susţină toate apărările în
            </p>
            <p>
                favoarea sa şi să ofere persoanei împuternicite să realizeze cercetarea,
                toate probele şi motivaţiile pe care le consideră necesare, precum şi
                dreptul de a fi asistat, la cererea sa, de către un reprezentant al
                sindicatului al cărui membru este.
            </p>
            <p>
                11.6.
            </p>
            <p>
                1).Angajatorul dispune aplicarea sancţiunii disciplinare printr-o decizie
                emisă în formă scrisă, în termen de 30 de zile calendaristice de la data
                luării la cunoştinţă despre săvârşirea abaterii disciplinare, dar nu mai
                târziu de 6 luni de la data săvârşirii faptei.
            </p>
            <p>
                2).Sub sancţiunea nulităţii absolute, în decizie se cuprind, în mod
                obligatoriu:
            </p>
            <p>
                a) descrierea faptei care constituie abaterea disciplinară;
            </p>
            <p>
                b) precizarea prevederilor din statutul de personal, regulamentul intern
                sau contractul colectiv de muncă aplicabil care au fost încălcate de
                salariat;
            </p>
            <p>
                c) motivele pentru care au fost înlăturate apărările formulate de salariat
                în timpul cercetării disciplinare prealabile sau motivele pentru care, în
                condiţiile prevăzute la art.11.5.alin.3) nu a fost efectuată cercetarea;
            </p>
            <p>
                d)temeiul de drept în baza căruia sancţiunea disciplinară se aplică;
            </p>
            <p>
                e)termenul în care sancţiunea poate fi contestată;
            </p>
            <p>
                f)instanţa competentă la care sancţiunea poate fi contestată.
            </p>
            <p>
                3).Decizia de sancţionare se comunică salariatului în cel mult 5 zile de la
                data emiterii şi produce efecte de la data comunicării.
            </p>
            <p>
                4).Comunicarea se predă personal salariatului, cu semnătura de primire ori,
                în caz de refuz al primirii, prin scrisoare recomandată, la domiciliu sau
                reşedinţa comunicată de acesta.
            </p>
            <p>
                5).Decizia de sancţionare poate fi contestată de salariat în instanţele
                judecătoreşti competente în termen de 30 de zile calendaristice de la data
                comunicării.
            </p>
            <p>
                11.7.Se consideră <strong>abateri disciplinare </strong>ale persoanelor
                angajate în societate următoarele fapte:
            </p>
            <p>
                1.-neîndeplinirea în mod repetat a obligaţiilor ce-i revin potrivit
                contractului individual de
            </p>
            <p>
                muncă şi R.I..
            </p>
            <p>
                2.-neutilizarea echipamentului individual de protecţie şi de lucru în
                timpul programului;
            </p>
            <p>
                3.-introducerea în unitate sau consumul în unitate de băuturi alcoolice;
            </p>
            <p>
                4.-prezentarea la locul de muncă sub influenţa băuturilor alcoolice sau în
                stare de ebrietate;
            </p>
            <p>
                5.-purtarea necuviincioasă faă de superiori;
            </p>
            <p>
                6.-întârzierea la serviciu sau absenţa de la locul de muncă. Nerespectarea
                programului de lucru;
            </p>
            <p>
                7.-plecarea de la locul de muncă pentru prima dată înainte de terminarea
                programului său în timpul programului de lucru fără aprobarea şefului
                ierarhic;
            </p>
            <p>
                8.-încălcarea secretului de serviciu ;
            </p>
            <p>
                9.-nerespectarea dispoziţiei şefilor ierarhici;
            </p>
            <p>
                10.-efectuarea în timpul programului de lucru a unor lucrări ce nu au
                legătură cu obligaţiile de serviciu;
            </p>
            <p>
                11.-nerespectarea ordinei şi curăţeniei;
            </p>
            <p>
                12.-încălcarea demnităţii personale a altor angajaţi, comitând acţiuni de
                discriminare;
            </p>
            <p>
                13.-lipsa vădită de interes în realizarea programului şi sarcinilor de
                muncă;
            </p>
            <p>
                14.-scoaterea din unitate fără interes de serviciu a bunurilor şi
                materialelor de orice fel în scopul folosirii lor în interesul personal sau
                a altora;
            </p>
            <p>
                15.-practicarea jocurilor de noroc în incinta unităţii;
            </p>
            <p>
                16.-însuşirea sau distrugerea de bunuri şi valori aparţinând societăţii;
            </p>
            <p>
                17.-sustragerea sub orice formă de bunuri şi valori aparţinând societăţii;
            </p>
            <p>
                18.-absentarea nemotivată la locul de muncă;
            </p>
            <p>
                19.-manifestarea vădită de neglijenţă în serviciu care determină risipa de
                materii prime, materiale, combustibili;
            </p>
            <p>
                20.-refuzul de a da explicaţii în legatură cu executarea obligaţiilor de
                serviciu la cererea şefului direct sau a altor persoane îndreptăţite sau
                răspunsurile obraznice în legătură cu aceasta;
            </p>
            <p>
                21.-încălcarea regulilor de comportare în relaţiile de serviciu,
                neîndeplinirea obligaţiilor de serviciu cuprinsă în fişa postului său
                stabilite de şeful direct sau îndeplinirea lor necorespunzătoare.
            </p>
            <p>
                22.-refuzarea supunerii controlului cu detector de alcool sau neefectuarea
                analizelor pentru determinarea concentraţiei de alcool în sânge;
            </p>
            <p>
                23.-nepontarea cartelei de pontaj la intrarea şi ieşirea de la locul de
                muncă.
            </p>
            <p>
                Enumerarea faptelor din cuprinsul acestui articol nu este limitativă,
                sancţiunile disciplinare putându-se aplica şi pentru nerespectarea altor
                prevederi din prezentul regulament.
            </p>
            <p>
                11.8.Se consideră de asemenea abateri disciplinare săvârşite de
                conducătorii ierarhici:
            </p>
            <p>
                -abuzul de autoritate faţă de personalul din subordine;
            </p>
            <p>
                -aplicarea în mod nejustificat a unor sancţiuni sau acordarea nejustificată
                de recompense.
            </p>
            <p>
                11.9.Neefectuarea preavizului de 20 zile, ca urmare a voinţei unilaterale a
                salariatului privind încetarea contractului individual de muncă, atrage
                sancţionarea salariatului.
            </p>
            <p class="marginT30">
                <strong>Cap XII . RĂSPUNDEREA PATRIMONIALĂ</strong>
            </p>
            <p>
                12.1 Salariaţii răspund patrimonial, în temeiul normelor şi principiilor
                răspunderii civile contractuale, pentru pagubele materiale produse
                angajatorului din vina şi în legatură cu munca lor.
            </p>
            <p>
                12.2 Salariaţii nu răspund de pagubele provocate de forţă majoră sau de
                alte cauze neprevăzute care nu puteau fi inlăturate şi nici de pagubele
                care se încadrează în riscul normal al serviciului.
            </p>
            <p>
                12.3 Ȋn situaţia în care angajatorul constată că salariatul sau a provocat
                o pagubă din vina şi în legătură cu munca sa, va putea solicita
                salariatului, printr-o notă de constatare şi evaluare a pagubei,
                recuperarea contravalorii acesteia, prin acordul părţilor, într-un termen
                stabilit de părţi.
            </p>
            <p>
                12.4 Contravaloarea pagubei recuperate prin acordul părţilor, conform alin.
                (3), nu poate fi mai mare decât echivalentul a 5 salarii minime brute pe
                economie.
            </p>
            <p class="marginT30">
                <strong>CAP XIII . PROTECŢIA MATERNITĂŢII LA LOCUL DE MUNCĂ</strong>
            </p>
            <p>
                13.1. Asigurarea igienei, protecţiei sănătăţii şi securitatea muncii.
            </p>
            <p>
                Ȋn ceea ce priveşte securitatea şi sănătatea în munca a salariatelor
                gravide şi/ sau mame, lauze sau care alăptează, angajatorul va asigura la
                locul de muncă măsuri privind igiena, protecţia sănătăţii si securitatea
                muncii a acestora conform prevederilor legale.
            </p>
            <p>
                13.2. Salariatele gravide şi/ sau mamele, lauzele sau care alăptează pentru
                a beneficia de aceste măsuri trebuie să informeze în scris angajatorul
                asupra stării lor fiziologice astfel:
            </p>
            <p>
                a) salariata gravidă va anunţa în scris angajatorul asupra stării sale
                fiziologice de graviditate, va depune în copie Anexa pentru supravegherea
                medicala a gravidei- document medical eliberat de medicul de familie sau de
                medicul specialist care să îi ateste această stare.
            </p>
            <p>
                b) Angajatorul are obligaţia ca în termen de 10 zile lucrătoare de la data
                la care a fost anunţat în scris de către salariată, să înştiinteze medicul
                de medicina muncii şi Inspectoratul Teritorial de Muncă.
            </p>
            <p class="marginT30">
                <strong>Cap.XIV. EVALUAREA ANGAJAŢILOR</strong>
            </p>
            <p>
                14.1.Scopul criteriilor de evaluare
            </p>
            <p>
                Criteriile de evaluare stabilesc cadrul general pentru asigurarea
                concordanţei dintre cerinţele postului, calităţile angajatului şi
                rezultatele muncii acestuia la un moment dat.Aplicarea prezentei proceduri
                acoperă cerinţele legale referitoare la evaluarea angajaţilor valabile la
                data întocmirii prezentei proceduri şi, de asemenea răspunde cerinţelor
                manageriale ale societăţii. Procedura se aplică:
            </p>
            <p>
                a) la angajare, pentru a aprecia măsura în care viitorul angajat poate
                răspunde cerinţelor de performanţă asociate postului pe care-l va ocupa;
            </p>
            <p>
                b) cu ocazia evaluărilor periodice, anuale, pentru a aprecia măsura în care
                angajatul satisface/ continuă să satisfacă cerinţele de performanţă
                asociate postului;
            </p>
            <p>
                c) ori de câte ori există o solicitare din partea managerului superior al
                salariatului, dacă se consideră ca angajatul trebuie evaluat pentru a
                aprecia măsura în care acesta îndeplineşte cerinţele de performanţă
                asociate postului;
            </p>
            <p>
                d) când apar modificări sau schimbări în organizaţie, determinate de
                creşterea sau restrângerea activităţii societăţii. Aceste modificări, pot
                presupune îmbogăţirea, restrângerea / modificarea cerinţelor asociate
                postului sau chiar desfiinţarea postului. Ca urmare se va aplica prezenta
                procedură pentru a se aprecia măsura în care angajatul poate să satisfacă
                noile cerinţe asociate postului ocupat, sau poate să ocupe un alt post din
                societate, sau se impune concedierea.
            </p>
            <p>
                14.2. Procesul de evaluare, evaluarea angajaţilor se realizează urmărind
                cerinţele stabilite în:
            </p>
            <p>
                -normele, cerinţele care trebuie respectate obligatoriu în societate Legea
                nr. 53/2003 – codul muncii
            </p>
            <p>
                -standarde, instrucţiuni de întreţinere a echipamentelor, sculelor,
                dispozitivelor, utilajelor pe care le utilizează
            </p>
            <p>
                -normele interne documentate (regulament intern, fişa postului, decizii
                documentate ale funcţiilor manageriale, instrucţiuni de lucru, alte norme
                documentate aprobate de Directorul General)
            </p>
            <p>
                14.3. Categoriile de evaluare aprobate de Director General sunt:
            </p>
            <p>
                1. DISCIPLINĂ şi ORDINE (disciplina, ordinea şi tinuta la muncă)
            </p>
            <p>
                2. RESPONSABILITATE (responsabilitate, relaţiile cu beneficiarii muncii
                sale)
            </p>
            <p>
                3. OBIECTIVE şi CALITATE (atingerea obiectivelor şi orientarea către
                calitate)
            </p>
            <p>
                4. ECHIPA (colegialitate şi spirit de echipă)
            </p>
            <p>
                5. COMPETENŢE (competenţe, abilităţi, însuşiri şi deprinderi cerute pentru
                post)
            </p>
            <p>
                6. CONDUCERE (criterii suplimentare pentru funcţiile de management)
            </p>
            <p>
                Se dă pentru fiecare criteriu de evaluare o nota de la 1 la 5:
            </p>
            <p>
                - nota 1 pentru “Nesatisfăcător”;
            </p>
            <p>
                - nota 2 pentru “Satisfăcător cu derogare”;
            </p>
            <p>
                - nota 3 pentru “Satisfăcător”;
            </p>
            <p>
                - nota 4 pentru “ Bine ”;
            </p>
            <p>
                - nota 5 pentru “ Foarte bine ”.
            </p>
            <p>
                Ȋnsumând notele pe criteriile de evaluare se va obţine nota pentru acea
                categorie.
            </p>
            <p>
                La final, evaluatorul va stabili o notă generală de evaluare care
                reprezintă suma notelor obţinute pe fiecare categorie de evaluare.
            </p>
            <p>
                Evaluatorul va aprecia performanţa evaluatului încadrând nota generală
                obţinută în funcţie de nivelul de performanţă admis. Astfel se va aprecia
                performanţa evaluatului ca:
            </p>
            <p>
                o Nesatisfăcător – nu îndeplineşte cerinţele minime asociate postului;
            </p>
            <p>
                o Satisfăcător cu derogare- îndeplineşte cerinţele minime prin derogare;
            </p>
            <p>
                o Satisfăcător – îndeplineşte cerinţele minime associate postului;
            </p>
            <p>
                o Bine – îndeplineşte toate cerinţele asociate postului;
            </p>
            <p>
                o Foarte bine – îndeplineste cerinţele asociate postului la nivel foarte
                bun.
            </p>
            <p>
                Procesul general de evaluare se încheie prin comunicarea rezultatului
                evaluării către evaluat şi către inspectorul de resurse umane.
            </p>
            <p>
                Comunicarea către evaluat se confirmă prin semnătura evaluatului.
            </p>
            <p>
                14.4. Evaluarea la angajare:
            </p>
            <p>
                Societatea evaluează viitorul angajat pentru a aprecia măsura în care
                acesta răspunde cerinţelor asociate postului vacant.
            </p>
            <p>
                Criteriile de evaluare la angajare sunt expresia cerinţelor menţionate, în
                principal, în fişele de post. Principalele criterii de evaluare la angajare
                sunt:
            </p>
            <p>
                o EDUCAŢIE (nivelul de educaţie şi specializarea generală);
            </p>
            <p>
                o CALIFICARE (calificări/specializări/cunoştinte specifice);
            </p>
            <p>
                o EXPERIENŢA (experienţa în muncă/ în specialitate);
            </p>
            <p>
                o ABILITĂŢI (abilităţi/ însuşiri psihologice cerute pentru ocuparea
                postului).
            </p>
            <p>
                Ȋn urma evaluării, evaluatul poate primi o notă de tipul “Admis/ Respins” .
                Comunicarea către evaluat se confirmă prin semnatura evaluatului.
            </p>
            <p class="marginT30">
                <strong>Cap.XV. DISPOZIŢII FINALE</strong>
            </p>
            <p>
                15.1.La părăsirea societăţii prin încetarea raportului de muncă, angajatul
                este obligat să restituie societaţii obiectele de inventar ce i-au fost
                încredinţate şi să lichideze toate datoriile faţă de societate.
            </p>
            <p>
                15.2.Prezentul regulament (R.I.) se completează cu prevederile- CODULUI
                MUNCII.
            </p>
            <p>
                15.3.Prezentul regulament va fi adus, prin grija conducerii societaţii, la
                cunoştinţa tuturor
            </p>
            <p>
                persoanelor angajate.
            </p>
            <p>
                15.4.Regulamentul de faţă poate fi modificat ori de câte ori necesităţile
                legale de organizare şi disciplina muncii sau legislaţia şi noile
                reglementări legale o impun.
            </p>
            <p>
                15.5.Prezentul REGULEMENT INTERN intră în vigoare la semnarea lui azi
                21.07.2016, înlocuind vechiul regulament şi orice alte dispoziţii contrare.
            </p>
